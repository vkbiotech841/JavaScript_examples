// Switch and case: Apply when multiple conditions are present.
// Applications: While throwing error in http response.
// 


let role = 'guest';

switch (role) {
    case 'guest':
        console.log('Guest User');
        break;

    case 'vikram':
        console.log('vikram logged')
        break;

    case 'Ricky':
        console.log('Ricky logged')
        break;

    default:
        console.log('Kriti logged')
};